#ifndef SRC_UID_GENERATOR_H
#define SRC_UID_GENERATOR_H


#include <cstdint>


////////////////////////////////////////////////////////////////////////////////
///////////////////////////////// Enumerations /////////////////////////////////
////////////////////////////////////////////////////////////////////////////////


enum class UID : uint32_t {};


////////////////////////////////////////////////////////////////////////////////
//////////////////////////////// UID Generator /////////////////////////////////
////////////////////////////////////////////////////////////////////////////////


class UID_Generator
{
private:

    /******************************* Variables ********************************/

    uint32_t uidCount {};

public:

    /***************************** Main Functions *****************************/

    template <typename T>
    UID getUID()
    {
        static const UID uid {uidCount++};
        return uid;
    }
};


#endif // SRC_UID_GENERATOR_H
