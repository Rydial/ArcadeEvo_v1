#ifndef SRC_UTIL_IMAGE_H
#define SRC_UTIL_IMAGE_H


#include <optional>
#include <string_view>


////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////// Structs ////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////


struct Image
{
    int width, height, composition;
    void* pixels;
};


////////////////////////////////////////////////////////////////////////////////
////////////////////////////////// Utilities ///////////////////////////////////
////////////////////////////////////////////////////////////////////////////////


namespace utilities
{
    /*
        Loads Image from Specified Path.
        Returns Image if Successful, else std::nullopt.
    */
    std::optional<Image> loadImage(std::string_view filePath);


    /*
        Free Given Image's Pixel Data
    */
    void freeImage(const Image& image);
}


#endif // SRC_UTIL_IMAGE_H
